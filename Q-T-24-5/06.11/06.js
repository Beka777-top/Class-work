let isRain = prompt ("Жаңбыр жауып турма?");

if (isRain == "иә" || isRain == 'Иә') {
    alert('Автобуспен бар');
} else if (isRain == 'Жоқ' || isRain ==  'жоқ') {

    var isMorning = prompt('Қазір таң ба?');

    if (isMorning == 'иә' || isMorning == 'Иә') {
        alert ('Велосипедпен бар');
    } else if (isMorning == 'Жоқ' || isMorning == 'Жоқ') {
        alert('Машинамен бар');
    }
}



