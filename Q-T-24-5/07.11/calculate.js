let number1 = Number (prompt("San engiziniz 1"))
let number2 = Number (prompt("San engiziniz 2"));
let calc = prompt("Tanba engiz")

switch(calc){
    case '+':
        alert(number1 + number2);
        break;
}

switch (calc) {
    case '/':
        alert(number1 / number2);
        break;
}

switch (calc) {
    case '*' :
        alert( number1 * number2);
        break;
}

switch (calc) {
    case '-' :
        alert(  number1  -  number2);
        break;
}
